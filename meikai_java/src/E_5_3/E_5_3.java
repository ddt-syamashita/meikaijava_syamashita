package E_5_3;

public class E_5_3 {

	public static void main(String[] args) {

		//boolean型変数を定義し、trueを代入する
		boolean trueFlg = true;

		//boolean型変数を定義し、falseを代入する
		boolean falseFlg = false;

		//それぞれのboolean型変数の値をコンソールに表示させるため、printを定義する
		System.out.println("trueFlg = " + trueFlg);
		System.out.println("falseFlg = " + falseFlg);

	}

}
