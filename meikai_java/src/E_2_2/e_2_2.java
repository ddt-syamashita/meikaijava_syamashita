package E_2_2;

public class e_2_2 {

	public static void main(String[] args) {

		//int型変数に値を代入する
		int value1 = 33;

		//int型変数に値を代入する
		int value2 = 44;

		//int型変数に値を代入する
		int value3 = 55;

		//変数x,y,zの合計値を出力する
		System.out.println(value1 + value2 + value3);

		//変数x,y,zの平均値を出力する
		System.out.println((value1 + value2 + value3) / 3);

	}

}
