package E_7_05;

public class E_7_05 {

	/**
	 *  指定された「こんにちは。」文言を出力させる
	 *  引数なし。
	 */
	static void hello() {
		System.out.println("『こんにちは。』");
	}

	public static void main(String[] args) {

		//helloメソッドを呼び出す
		hello();

	}

}
