package E_1_3;

public class e_1_3 {

	public static void main(String[] args) {

		//自分の苗字1文字目を出力後、改行させる
		System.out.println("山");

		//自分の苗字2文字目を出力後、改行させる
		System.out.println("下");

		//1行分改行する
		System.out.println();

		//自分の名前1文字目を出力後、改行させる
		System.out.println("星");

		//自分の名前2文字目を出力後、改行させる
		System.out.println("夜");


	}

}
